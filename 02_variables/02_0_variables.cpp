#include <iostream>
using namespace std;

int main(void)
{
    // integer
    int i = 6;

    // char
    char c = 'B';

    // float
    float f = 6.78;

    // double
    double d = 784.9736382;

    // bool
    bool t = true;
    bool b = false;
    bool z = 0;
    bool o = 1;



    // printing
    cout<<"Int: "<<i<<endl;
    cout<<"char: "<<c<<endl;
    cout<<"float: "<<f<<endl;
    cout<<"double: "<<d<<endl;
    cout<<"bool true: "<<t<<endl;
    cout<<"bool false: "<<b<<endl;
    cout<<"bool 0: "<<z<<endl;
    cout<<"bool 1: "<<o<<endl;

    return 0;
}